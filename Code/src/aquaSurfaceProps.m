% Centroid Sub-Function

function [Ai, Pi] = cent(As, Ps)

Ai = sum(As);

x = sum(Ps(:,1).*As)./Ai;
y = sum(Ps(:,2).*As)./Ai;
z = sum(Ps(:,3).*As)./Ai;

Pi = [x, y, z];

end

end